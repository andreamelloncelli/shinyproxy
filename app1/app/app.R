



# --------------------------------------------------------------- #
# ------------------------      GLOBAL  --------------------------#
# --------------------------------------------------------------- #

in_data <- read.delim('/root/data/in_data.txt')

# --------------------------------------------------------------- #
# ------------------------      UI      --------------------------#
# --------------------------------------------------------------- #
ui <- fluidPage(

  br(),
  fluidRow(

    column(width = 8,
           textOutput("row_count"))
    )
)

# --------------------------------------------------------------- #
# -----------------------  SERVER       --------------------------#
# --------------------------------------------------------------- #
server <- function(input, output, session) {
  output$row_count <- renderText(paste( 'Number of rows is',  nrow(in_data)))
}

# --------------------------------------------------------------- #
# ----------------------------  RUN ------------------------------#
# --------------------------------------------------------------- #
shinyApp(ui, server)

